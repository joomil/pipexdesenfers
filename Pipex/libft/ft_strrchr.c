#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*p;

	p = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			p = s;
		s++;
	}
	if (c == '\0')
		p = s;
	return ((char *)p);
}
